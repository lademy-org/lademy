


$("#neuf").on("click", function () {
  var neuf = $("#neuf");
  $(".list2").on("click", "li", function () {
    if (neuf.val() === $(this).val()) {
      neuf.css("color", "green");
      $(this).css("color", "green");
    } else {
      neuf.css("color", "red");
      $(this).css("color", "red");
    }
  });
});

